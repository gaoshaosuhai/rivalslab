export function heroById(id: string): Hero | undefined {
  return HEROES.find((h) => h.id === id);
}

export function heroesByRole(role: Role): Hero[] {
  return HEROES.filter((h) => h.role === role).sort((a, b) => a.name.localeCompare(b.name));
}

